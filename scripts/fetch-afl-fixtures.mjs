#!/usr/bin/env node
/**
 * fetch-afl-fixtures.mjs
 *
 * Builds src/data/aflFixtureMap.json: a mapping from football-data.org match
 * IDs (fdMatchId) to API-Football fixture IDs (AFL fixture ID) for WC 2026.
 *
 * How it works:
 *   1. Reads src/data/fixtures.json (OpenFootball schedule) to get the list of
 *      matches with their team names and dates.
 *   2. Fetches GET https://v3.football.api-sports.io/fixtures?league=1&season=2026
 *      to get the AFL fixture list.
 *   3. Matches each AFL fixture to a football-data.org fixture by normalising
 *      team names and comparing dates (±1 day tolerance).
 *   4. Writes { "fdMatchId": aflFixtureId } to src/data/aflFixtureMap.json.
 *
 * Usage:
 *   AFL_API_KEY=<key> node scripts/fetch-afl-fixtures.mjs
 *
 * Note: the API-Football free plan currently restricts access to seasons
 * 2022-2024. This script will produce an empty map until season=2026 becomes
 * available on the plan. Re-run once access is granted.
 *
 * The football-data.org match IDs (fdMatchId) are populated at runtime by
 * useLiveScores.ts once live score data is fetched. This script cannot pre-map
 * them without first knowing the FD match IDs; run it after the tournament
 * starts and FD IDs are known, or pair with fetch-fd-fixtures.mjs.
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));

const AFL_BASE = 'https://v3.football.api-sports.io';
const OUT_PATH = resolve(__dirname, '../src/data/aflFixtureMap.json');
const FIXTURES_PATH = resolve(__dirname, '../src/data/fixtures.json');

const key = process.env.AFL_API_KEY;
if (!key) {
  console.error('Error: AFL_API_KEY environment variable is required.');
  process.exit(1);
}

/** Normalise team names for fuzzy matching (lowercase, remove non-alpha). */
function norm(name) {
  return name.toLowerCase().replace(/[^a-z]/g, '');
}

/** Parse "YYYY-MM-DD" into a UTC midnight Date. */
function parseDate(str) {
  return new Date(str + 'T00:00:00Z');
}

/** Date difference in days between two Date objects. */
function diffDays(a, b) {
  return Math.abs(a.getTime() - b.getTime()) / (1000 * 60 * 60 * 24);
}

async function main() {
  console.log('Reading local fixture schedule from fixtures.json …');
  const rawData = JSON.parse(readFileSync(FIXTURES_PATH, 'utf8'));
  const localMatches = rawData.matches.map((m, i) => ({
    // Use match num if available, otherwise index (mirrors processFixtures.ts logic)
    localId: m.num ? `m${m.num}` : `m${i + 1}`,
    team1Norm: norm(m.team1 ?? ''),
    team2Norm: norm(m.team2 ?? ''),
    date: parseDate(m.date),
  }));

  console.log(`Fetching WC 2026 fixtures from API-Football (league=1, season=2026) …`);
  const res = await fetch(`${AFL_BASE}/fixtures?league=1&season=2026`, {
    headers: { 'x-apisports-key': key },
  });

  const data = await res.json();

  if (data.errors && Object.keys(data.errors).length > 0) {
    console.warn('API-Football returned errors:', JSON.stringify(data.errors));
    console.warn(
      'The free plan does not currently support season=2026. ' +
        'Writing empty map. Re-run once access is available.'
    );
    const out = {
      _note:
        'Maps football-data.org match IDs to API-Football fixture IDs for WC 2026. ' +
        'Generated by scripts/fetch-afl-fixtures.mjs. Empty until the free plan gains ' +
        'access to season=2026 (currently 2022-2024 only).',
      _fetchedAt: new Date().toISOString().slice(0, 10),
    };
    writeFileSync(OUT_PATH, JSON.stringify(out, null, 2) + '\n', 'utf8');
    console.log('Wrote empty map to', OUT_PATH);
    return;
  }

  const aflFixtures = data.response ?? [];
  console.log(`Retrieved ${aflFixtures.length} AFL fixtures.`);

  // Build the map: localId -> aflFixtureId
  // Note: we map by local OpenFootball ID here. The fdMatchId mapping
  // requires the football-data.org IDs which are only known at runtime.
  // This map is keyed by localId for now; update if FD IDs are known.
  const fixtureMap = {
    _note:
      'Maps football-data.org match IDs (fdMatchId) to API-Football fixture IDs ' +
      'for WC 2026. Generated by scripts/fetch-afl-fixtures.mjs.',
    _fetchedAt: new Date().toISOString().slice(0, 10),
    _aflFixtureCount: aflFixtures.length,
  };

  let matched = 0;
  for (const afl of aflFixtures) {
    const aflHome = norm(afl.teams?.home?.name ?? '');
    const aflAway = norm(afl.teams?.away?.name ?? '');
    const aflDate = new Date(afl.fixture?.date ?? '');

    const local = localMatches.find(
      (m) =>
        m.team1Norm === aflHome &&
        m.team2Norm === aflAway &&
        diffDays(m.date, aflDate) <= 1
    );

    if (local) {
      fixtureMap[local.localId] = afl.fixture.id;
      matched++;
    } else {
      // Log unmatched AFL fixtures for debugging
      console.warn(
        `  No local match for AFL fixture ${afl.fixture.id}: ${afl.teams?.home?.name} vs ${afl.teams?.away?.name} on ${afl.fixture?.date}`
      );
    }
  }

  writeFileSync(OUT_PATH, JSON.stringify(fixtureMap, null, 2) + '\n', 'utf8');
  console.log(
    `Matched ${matched}/${aflFixtures.length} AFL fixtures. Wrote map to ${OUT_PATH}`
  );
}

main().catch((err) => {
  console.error('fetch-afl-fixtures failed:', err.message);
  process.exit(1);
});
